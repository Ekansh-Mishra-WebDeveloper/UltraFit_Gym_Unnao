const express = require('express');
const router = express.Router();
const WorkoutDay = require('../models/WorkoutDay');

router.get('/', async (req, res) => {
  try {
    const days = await WorkoutDay.find().sort('order');
    res.json(days);
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

router.post('/', async (req, res) => {
  try {
    const day = new WorkoutDay(req.body);
    await day.save();
    res.status(201).json(day);
  } catch (err) {
    res.status(400).json({ error: err.message });
  }
});

router.put('/:id', async (req, res) => {
  try {
    const updated = await WorkoutDay.findByIdAndUpdate(req.params.id, req.body, { new: true });
    res.json(updated);
  } catch (err) {
    res.status(400).json({ error: err.message });
  }
});

router.delete('/:id', async (req, res) => {
  try {
    await WorkoutDay.findByIdAndDelete(req.params.id);
    res.json({ message: 'Deleted' });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

module.exports = router;